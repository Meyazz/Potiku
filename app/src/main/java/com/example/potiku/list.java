package com.example.potiku;

public class list {

    private String id;
    private String nama;
    private String jenis;

    public list() {
        this.id = id;
        this.nama = nama;
        this.jenis = jenis;

    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getJenis() {
        return jenis;
    }

    public void setJenis(String jenis) {
        this.jenis = jenis;
    }
}